    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        float cc = Float.parseFloat(txtCC.getText());
        float gk = Float.parseFloat(txtGK.getText());
        float ck = Float.parseFloat(txtCK.getText());
        if (cc>=0 && cc<=10 && gk>=0 && gk<=10 && ck>=0 && ck<=10)
        {
            try {
                // TODO add your handling code here:
                String result = connectBtnSubmit(txtMSSV.getText(), boxSubjects.getSelectedItem().toString(), txtCC.getText(), txtGK.getText(), txtCK.getText());
                if (result.equals("OK")) {
                    JOptionPane.showMessageDialog(null,"Bạn đã cập nhật điểm thành công");
                    dispose();
                }
                else {
                    JOptionPane.showMessageDialog(null,"Cập nhật điểm thất bại");
                    dispose();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
        else {
            JOptionPane.showMessageDialog(null,"Vui lòng nhập điểm trong khoảng 0 đến 10");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

