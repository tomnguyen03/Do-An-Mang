    private String connectBtnSubmit(String mssv, String monHoc,String CC,String GK, String CK) throws Exception {
        Socket sk = connect();
        DataOutputStream dos = new DataOutputStream(sk.getOutputStream());
        DataInputStream din = new DataInputStream(sk.getInputStream());
        String data = "BtnSubmit\n"+mssv+"\n"+monHoc+"\n"+CC+"\n"+GK+"\n"+CK+"\n";
        System.out.println(data);
        dos.writeUTF(data);
        String result = din.readUTF();
        System.out.println("Server: "+result);
        sk.close();
        return result;
    }
